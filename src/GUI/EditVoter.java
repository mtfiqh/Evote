    private void updateData(){
        String query="UPDATE `mahasiswa` SET `NIM`='"+NIMganti+"',`Nama`='"+Nama+"',`kd_prodi`='"+kd_prodi+"' WHERE NIM='"+NIM+"'";
        Connection con = datacon.getConnection();
        try{
            st=con.createStatement();
            if(st.executeUpdate(query)==1){
                JOptionPane.showMessageDialog(this, "Data Updated!");
                voter.ShowData("SELECT * FROM mahasiswa");
                dispose();
                voter.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(this, "Data not Updated!");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Data not Updated!");
            e.printStackTrace();
        }
    }
