    void timerOn(){
        timer = new Timer(1000, new ActionListener() {
           
            @Override
            public void actionPerformed(ActionEvent ae) {
                countTimer--;
                labelTimer.setText(Integer.toString(countTimer));
                if(countTimer==0){
                    
                    Connection con = datacon.getConnection();
                    Statement st=null;
                    try{
                        st=con.createStatement();
                        if(st.executeUpdate("UPDATE mahasiswa set keterangan=1 WHERE NIM='"+nimMahasiswa+"'")==1){
                            if(st.executeUpdate("DELETE from loginvoter where username='"+nimMahasiswa+"'")==1 ){
                                dispose();
                                JOptionPane.showMessageDialog(null, "Anda golput!");
                                LoginForm login = new LoginForm();
                                login.setVisible(true);
                                login.setLocationRelativeTo(null);
                        }
                    }
                    }catch(SQLException e){
                        e.printStackTrace();
                    }
                    timer.stop();
                }
            }
        });
       timer.start();
    }
    public void ShowData(String query){
        list = listArray.getListDataCalon(query);
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        System.out.println("Load!");
        model.setRowCount(0);
        Object[] row=new Object[3];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getNim();
            row[1]=list.get(i).getNama();
            row[2]=list.get(i).getKeterangan();
            model.addRow(row);
        }
        int sizeData=jTable1.getRowCount();
        String temp=String.valueOf(sizeData);
        jLabel5.setText("Banyak Data: "+temp);
    }
    
